
public class NegativeArgumentException extends Exception {

    public NegativeArgumentException() {
        super();
    }

    public NegativeArgumentException(String s) {
        super(s);
    }

    public NegativeArgumentException(String s, Throwable throwable) {
        super(s, throwable);
    }

    public NegativeArgumentException(Throwable throwable) {
        super(throwable);
    }

    public NegativeArgumentException(String s, Throwable throwable, boolean b, boolean b1) {
        super(s, throwable, b, b1);
    }
}
